    public boolean verifyFields(){
        String uname = jTextField_Username.getText();
        String pass = String.valueOf(jPasswordField.getPassword());
        String cpass = String.valueOf(jPasswordField1.getPassword());
        
        if(uname.trim().equals("") || pass.trim().equals("") || cpass.trim().equals("")){
            JOptionPane.showMessageDialog(null, "Không để trống");
            return false;
        }else if(!pass.equals(cpass)){
            JOptionPane.showMessageDialog(null, "Mật khẩu không trùng khớp");
            return false;
        }
        else{
            return true;
        }
    }
    //check if usename exists in the database
    public boolean checkUsername(String username){
        try {
            databaseSocket = new Socket("localhost", 12345);  
            
            databaseOut = new PrintWriter(new OutputStreamWriter(databaseSocket.getOutputStream()), true);
            databaseIn = new BufferedReader(new InputStreamReader(databaseSocket.getInputStream()));
            databaseOut.println("CHECK_USERNAME");
            databaseOut.println(username);
            boolean username_exist = false;
            
            String response = databaseIn.readLine();
             
            if ("CHECK_SUCCESS".equals(response)) {
                JOptionPane.showMessageDialog(null, "Tên đã tồn tại");
                return username_exist = true;   
            } else {
            // Hiển thị thông báo lỗi nếu đăng nhập thất bại
                JOptionPane.showMessageDialog(this, "tên đăng nhập hoặc mật khẩu sai", "Login Failed", JOptionPane.ERROR_MESSAGE);
                
            }              
        } catch (Exception e) {
            e.printStackTrace();
        }          
        return false;
    }
