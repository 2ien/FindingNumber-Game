    private void jButton_RegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_RegisterActionPerformed
        try {
            databaseSocket = new Socket("localhost", 12345);  
            
            databaseOut = new PrintWriter(new OutputStreamWriter(databaseSocket.getOutputStream()), true);
            databaseIn = new BufferedReader(new InputStreamReader(databaseSocket.getInputStream()));
            
            String username = jTextField_Username.getText();//Get the username and pass
            String pass = String.valueOf(jPasswordField.getPassword());
            String cpass = String.valueOf(jPasswordField1.getPassword());
            String gender = "Male";
            if(jRadioButton_Female.isSelected()){
                gender = "Female";
            }
             
            if(verifyFields()){
                if(!checkUsername(username)){
                    PreparedStatement ps;
                    ResultSet rs;
                    String registerUserQuery = "INSERT INTO `users`(`username`, `password`, `gender`, `picture`) VALUES (?,?,?,?)";

                    try {
                        ps = ClientHandler.getConnection().prepareStatement(registerUserQuery);
                        ps.setString(1, username);
                        ps.setString(2, pass);
                        ps.setString(3, gender);                 
                        try {
                            //save the image as blob in the database
                            if(image_path != null){
                                  InputStream image = new FileInputStream(new File(image_path));
                                  ps.setBlob(4, image);
                            } else {    
                                  ps.setNull(4,java.sql.Types.NULL);
                            } 
                            if(ps.executeUpdate() != 0){
                                JOptionPane.showMessageDialog(null,"Tài khoản đã tạo");
                            }else {
                                JOptionPane.showMessageDialog(null,"Lỗi : Kiểm tra thông tin của bạn");
                            }

                        } catch (FileNotFoundException ex) {
                            Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    } catch (SQLException ex) {
                        Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton_RegisterActionPerformed

